export function getViewportDesignTokens(meta = {}) {
  const designWidth = Number(meta.design_width) || REPORT_DESIGN_WIDTH
  const designHeight = Number(meta.design_height) || REPORT_DESIGN_HEIGHT
  return {
    designWidth,
    designHeight,
    style: {
      '--report-design-width': `${designWidth}px`,
      '--report-design-height': `${designHeight}px`,
      '--report-page-padding-x': '24px',
      '--report-page-padding-top': '22px',
      '--report-page-padding-bottom': '28px',
    },
  }
}

export function getPageThemeStyle(pageId) {
  const theme = PAGE_THEME_MAP[pageId] || PAGE_THEME_MAP.P01
  return {
    '--page-design-height': `${REPORT_DESIGN_HEIGHT}px`,
    '--page-accent': theme.accent,
    '--page-accent-strong': theme.accentStrong,
    '--page-accent-soft': theme.accentSoft,
    '--page-background-start': theme.backgroundStart,
    '--page-background-end': theme.backgroundEnd,
    '--page-surface-glow': theme.surfaceGlow,
    '--page-surface-glow-secondary': theme.surfaceGlowSecondary,
  }
}
